#include "CorePCH.h"

#include "MaterialFactory.h"

//////////////////////////////////////////////////////////////////////////

CMaterialFactory* CMaterialFactory::ms_pkInstance = 0;

//////////////////////////////////////////////////////////////////////////

CMaterial* CMaterialFactory::CreateMaterial(
	const stl_string& sMaterialName)

{
	if (ms_pkInstance == 0)
		return 0;

	return ms_pkInstance->Do_CreateMaterial(sMaterialName);
}

